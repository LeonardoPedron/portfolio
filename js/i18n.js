class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('preferredLanguage') || 'it';
        this.translations = translations;
    }

    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('preferredLanguage', lang);
            this.updatePage();
            document.documentElement.lang = lang;
        }
    }

    t(path) {
        const keys = path.split('.');
        let value = this.translations[this.currentLang];

        for (const key of keys) {
            if (value && typeof value === 'object') {
                value = value[key];
            } else {
                return path;
            }
        }

        return value || path;
    }

    updatePage() {
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.t(key);

            if (element.hasAttribute('data-i18n-html')) {
                element.innerHTML = translation;
            } else {
                element.textContent = translation;
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            element.placeholder = this.t(key);
        });

        if (window.typingEffect) {
            window.typingEffect.updateTitles(this.t('hero.titles'));
        }

        document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: this.currentLang } }));
    }

    getCurrentLang() {
        return this.currentLang;
    }
}

const i18n = new I18n();
window.i18n = i18n;
